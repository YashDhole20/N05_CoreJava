package org.tnsif.wrapperClassDemo;

//Program to demonstrate on Autoboxing
//AutoBoxing : conversion of primitive type to object type
public class Autooxing {
	public static void main(String[]arg) {
		char c='c';
		//converting a primitive data type into  object type 
		Character ch=c;
		System.out.println(ch);
	}
}
